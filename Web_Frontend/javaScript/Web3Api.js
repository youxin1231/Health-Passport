function HPcontract(HPabi, HPaddress){
  const contract = new web3.eth.Contract(HPabi, HPaddress);
  // 查看現有HP總供應量 (病例數量)
  contract.methods.totalSupply().call((err, result) => { console.log("現有HP總量: " + result) })

  // 查看該地址的HP數量 (病例數量)
  const add01 = "0xB32eCd6BDBB414e7e6Cb244366aa3fCee6A12911";
  contract.methods.balanceOf(add01).call((err, result) => { console.log("該地址的HP數量: " + result) })

  // 查看該地址的病例URI (連結,資訊)
  const add02 = "0xB32eCd6BDBB414e7e6Cb244366aa3fCee6A12911";
  contract.methods.tokenURIOfOwner(add02).call((err, result) => { console.log("該地址的病例URI: " + result) })
}

